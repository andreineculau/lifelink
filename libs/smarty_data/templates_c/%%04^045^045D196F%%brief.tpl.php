<div id="llpagetitle">In Brief</div>
<h2>Life-Link Friendship-Schools is an independent Non Governmental Organisation which aims to promote contact and cooperation between young people around the world and their schools, through active participation in shared projects, vital for our time (e.g. Environment, Human Rights, Conflict Resolution and Constructive Collaboration).</h2>
<p>Life-Link projects centre around three main areas of attention: <strong>Care for ourselves</strong> - <strong>Care for each other</strong> - <strong>Care for the environment</strong>. Realisation of these three interdependent areas will lead to increased common security.</p>
<p>The Life-Link philosophy is <strong>based on Natural and Social sciences</strong> and is <strong>neither politically nor religiously</strong> aligned.</p>
<p>Life-Link is today a well recognized non-governmental organisation with international contacts <strong>in more than 60 countries</strong> worldwide.</p>
<span class="llclear" style="height: 2em"></span>
