<h2>Theory</h2>
<h3>How to take an initiative, get an idea?</h3>
<blockquote>
	<p>&quot;What sort of concerns, needs and problems to do with organisational aims and objectives do you want to do something about?&quot;</p>
	<p>&quot;Think globally, act locally&quot;.</p>
	<p>What do you want to improve?
	<ul>
		<li>Ask team to think about problems.</li>
		<li>Make a list of ideas to bring to the meeting.</li>
		<li>Team members put ideas forward at the meeting.</li>
		<li>List all ideas.</li>
		<li>Organise ideas into clusters.</li>
		<li>Prioritise clusters of ideas. (What do you think is most important? What can you handle practically?)</li>
		<li>Make sure other parts of the organisation are kept informed.</li>
		<li>Ensure regular communication of progress of team to organisation.</li>
	</ul>
	</p>
</blockquote>
<br>
<h2>Definition</h2>
<h3>Definition of the desired results</h3>
<blockquote>
	<p>Make a decision on the scope of the project, gain agreement from relevant management, define as concrete and as clearly as possible what the outcomes of the project will be.
	<ul>
		<li>Be as specific as possible on proposed outcomes.</li>
		<li>Record decisions you have made, report sent to management.</li>
	</ul>
	</p>
</blockquote>
<br>
<h2>Design</h2>
<blockquote>
	<p>Central question of this step is to find solutions for the problem you want to resolve or the concern you want to work on. In what way or form are you going to tackle the issue?
	<ul>
		<li>Generate as many possible solutions as possible.</li>
		<li>Creativity stage, brainstorm sessions, inventories, mapping sessions.</li>
		<li>Selection of most feasible, effective, efficient solutions to the problems at hand.</li>
		<li>Decision on what form the project will take.</li>
	</ul>
	</p>
</blockquote>
<br>
<h2>Preparation</h2>
<h3>Preparation, planning of activities</h3>
<blockquote>
	<p>Defining the outcomes and making decisions about the form of the project enables team to choose the activities that need to be carried out and in what sequence. &quot;Think before you act!&quot;. &quot;Work from the broad to the fine&quot;. Use five control factors:
	<ul>
		<li>Time - set project time limits.</li>
		<li>Money - make a budget beforehand.</li>
		<li>Quality - define quality standards, quantifiable, measureable, margins.</li>
		<li>Information - keep information flowing to all team members plus others.</li>
		<li>Organisation - list target audience, resources, subcommittees, workgroups, tasks, roles etc.</li>
	</ul>
	</p>
</blockquote>
<br>
<h2>Realisation</h2>
<h3>Action</h3>
<blockquote>
	<p>Execute project to achieve required results in scheduled time limits.
	<ul>
		<li>Write short report - activities and results.</li>
	</ul>
	</p>
</blockquote>
<br>
<h2>Follow Up</h2>
<blockquote>
	<p>Evaluate - what did we do well and what can we do in the future? etc.</p>
	<p>Follow up - what can be done in order to consolidate? Write and report next step. A full report on the project can also be put together if of value.</p>
</blockquote>
