<div id="llpagetitle">Step 2 - Definition of the desired results</div>
<p>Once the committee has decided what problem or need will be tackled and the school authorities have agreed on it, the next step is to define as concrete and clear as possible what the outcomes of the project will be.</p>
<p>In formulating the results of the project try to be as specific as possible. For example, if your project is centred around care for the environment, you would not be satisfied by just saying: &quot;We want the school community to become more aware of the need to keep a clean environment.&quot; It will be very difficult at the end of the project to know, if you have reached such a result or not.</p>
<p>Instead you would rather use formulation of project results such as:</p>
<blockquote>
	<ul>
		<li>we want the littering of the school grounds be reduced by 50% over a period of time, e.g. 2 months</li>
		<li>we want at least 80% of the pupils of our school to rate care for the school environment as an important area of our school policies, e.g. by December 1st.</li>
	</ul>
</blockquote>
<p>Formulate the decisions you have been taken regarding the outcomes of your projects and write them on paper. Make sure you send a copy of your document to the school principal or delegated authority.</p>
