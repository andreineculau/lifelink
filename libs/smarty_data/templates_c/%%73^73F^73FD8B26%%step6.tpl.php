<div id="llpagesuptitle">Project Management</div>
<div id="llpagetitle">Step 6 - Follow up</div>
<p>This step consists of two basic elements. First, all the steps undertaken in the project are being evaluated.<br>
In the evaluation the committee tries to answer questions such as:</p>
<ul>
	<li>how did each step come about?</li>
	<li>what did we do really well and what can be improved in the future?</li>
	<li>what did we learn from the project?</li>
	<li>how was cooperation among the members of the committee?</li>
	<li>how was the relation with the school authorities?</li>
</ul>
<p>The second element is looking into the possibilities of follow up. Now that the project has been finished what can be done in order to consolidate the results?<br>
Three options can be considered here:</p>
<ul>
	<li> the outcomes of the project will have become normal procedures of the school;</li>
	<li> a follow up project will start in order to further consolidate results or realise certain desired alterations and/ or improvements as a result of the evaluation;</li>
	<li> the project was a one time thing, there is no follow up or consolidation, no routine has been established, no follow up project will start.</li>
</ul>
<p>The results of the evaluation and decisions regarding follow up are written down on paper and reported to the school authorities. If desired they are published in the school bulletin.</p>
<p>If there is a demand for it, the documents that have been produced at the end of each stage, can be brought together in an overall report regarding the project. Such a report could be stored in the school library for future Life-Link project work in the school.</p>
<p>A short report is sent to the Life-Link International Office.</p>
